import turtle
import math

tortue = turtle.Turtle()

tortue.forward(200)
tortue.left(90)     # Rotate clockwise by 90 degrees

tortue.forward(200)
tortue.left(90)

tortue.forward(200)
tortue.left(90)

tortue.forward(200)
tortue.left(135)

tortue.forward(math.sqrt(80000))
tortue.left(135)

tortue.forward(200)
tortue.left(135)

tortue.forward(math.sqrt(80000))

turtle.done()